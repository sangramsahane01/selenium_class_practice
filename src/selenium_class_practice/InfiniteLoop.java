package selenium_class_practice;

public class InfiniteLoop {

	public static void main(String[] args) { 
		
		 /***Below Is First Code for Infinite Loop***/
		  
		   int i=1;
		    while(i==1) {
		    	
		    	System.out.println("Hello");
		    }
		    
		 /***Below Is Second Code for Infinite Loop***/
		  for(;;) { 
			  
			  System.out.println("Hello"); }
	
		 
	  }
}
